const car = 'Audi'
switch (car) {
    case 'VW':
    case 'Audi':
    case 'Bentley':
    case 'Lamborghini':
    case 'Porsche':
        console.log("VW group");
        break;
    case 'BMW':
    case 'Mini':
    case 'Rolls-Royce':
        console.log('BMW group');
        break;
    default:
        console.log("Nei VW group, nei BMW group")
}

